let numeroDia = Number(prompt('Ingrese número del día'))

switch (numeroDia) {
  case 1:
    alert('El día es lunes')
    break
  case 2:
    alert('El día es martes')
    break
  case 3:
    alert('El día es miercoles')
    break
  case 4:
    alert('El día es jueves')
    break
  case 5:
    alert('El día es viernes')
    break
  case 6:
    alert('El día es sábado')
    break
  case 7:
    alert('El día es domingo')
    break
  default:
    alert('El día ingresado no es válido')
    break
}
